def h2(title):
    pdf.ln(2)
    pdf.set_font("Helvetica", "B", 13)
    pdf.set_text_color(*PURPLE)
    pdf.cell(0, 8, clean(title), ln=1)
    pdf.set_draw_color(220, 215, 230)
    y = pdf.get_y()
    pdf.line(18, y, 192, y)
    pdf.ln(2)


def body(text):
    pdf.set_font("Helvetica", "", 10)
    pdf.set_text_color(*DARK)
    pdf.multi_cell(0, 5.2, clean(text))
    pdf.ln(1)


def item(label, lines, tag=None, tag_color=GREY):
    pdf.set_font("Helvetica", "B", 10.5)
    pdf.set_text_color(*DARK)
    if tag:
        pdf.set_font("Helvetica", "B", 7.5)
        pdf.set_text_color(255, 255, 255)
        pdf.set_fill_color(*tag_color)
        w = pdf.get_string_width(tag) + 4
        pdf.cell(w, 5, tag, align="C", fill=True)
        pdf.cell(2, 5, "")
    pdf.set_font("Helvetica", "B", 10.5)
    pdf.set_text_color(*DARK)
    pdf.multi_cell(0, 5.4, clean(label))
    pdf.set_font("Helvetica", "", 9.5)
    pdf.set_text_color(*GREY)
    for ln_ in lines:
        pdf.set_x(22)
        pdf.multi_cell(0, 4.8, clean("- " + ln_))
    pdf.ln(1.5)


