def clean(s: str) -> str:
    repl = {
        "—": "-", "–": "-", "→": "->", "‘": "'",
        "’": "'", "“": '"', "”": '"', "…": "...",
        "·": "-", "✓": "[x]", "✕": "x", "é": "e",
        "•": "-",
    }
    for k, v in repl.items():
        s = s.replace(k, v)
    return s.encode("latin-1", "replace").decode("latin-1")


class PDF(FPDF):
    def header(self):
        if self.page_no() == 1:
            return
        self.set_font("Helvetica", "", 8)
        self.set_text_color(*LIGHTGREY)
        self.cell(0, 8, "Orator - Website Change Report", align="L")
        self.cell(0, 8, f"Page {self.page_no()}", align="R")
        self.ln(10)

    def footer(self):
        self.set_y(-14)
        self.set_font("Helvetica", "", 7)
        self.set_text_color(*LIGHTGREY)
        self.cell(0, 8, clean("Generated by Claude Code - claude/relaxed-sagan-nvpch5"), align="C")


